#include "libft.h"

size_t	ft_wcount(const char *s, char c)
{
	size_t i;

	i = 0;
	while (1)
	{
		while (*s == c && *s)
			s++;
		if (!*s)
			break ;
		i++;
		while (*s != c && *s)
			s++;
		if (!*s)
			break ;
	}
	return (i);
}
